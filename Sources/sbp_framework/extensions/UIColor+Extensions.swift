import Foundation
import UIKit

extension UIColor {
    @nonobjc class var primary: UIColor {
      return UIColor(red: 245 / 255.0, green: 241.0 / 255.0, blue: 232.0 / 255.0, alpha: 1)
    }
    @nonobjc class var lightGray: UIColor {
      return UIColor(red: 213 / 255.0, green: 213.0 / 255.0, blue: 214.0 / 255.0, alpha: 1)
    }
}
